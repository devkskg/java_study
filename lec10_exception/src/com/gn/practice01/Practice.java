package com.gn.practice01;

public class Practice {
	private int a;
	private int b;

	public Practice() {
	}

	public Practice(int a, int b) {
		this.a = a;
		this.b = b;
	}

	public int add() {
		return a + b;
	}

	public int multiple() {
		return a * b;
	}

	public int divide() {
		return a / b;
	}

	public int add(int a, int b) {
		return a + b;
	}

	public int multiple(int a, int b) {
		return a * b;
	}

	public int divide(int a, int b) {
		return a / b;
	}
}
